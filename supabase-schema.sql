-- ═══════════════════════════════════════════════════════
-- STORAGE POLICIES (Optional - Only if using Supabase Storage)
-- ═══════════════════════════════════════════════════════
-- Run these after creating the 'gallery-images' bucket in the dashboard

DROP POLICY IF EXISTS "Public read access to gallery-images"    ON storage.objects;
DROP POLICY IF EXISTS "Authenticated upload to gallery-images"  ON storage.objects;
DROP POLICY IF EXISTS "Users can delete own images"             ON storage.objects;

-- Anyone can view images (needed for gallery display)
CREATE POLICY "Public read access to gallery-images"
  ON storage.objects FOR SELECT
  USING (bucket_id = 'gallery-images');

-- Authenticated users can upload images
CREATE POLICY "Authenticated upload to gallery-images"
  ON storage.objects FOR INSERT
  WITH CHECK (
    bucket_id = 'gallery-images' AND
    auth.role() = 'authenticated'
  );

-- Users can only delete their own images (folder = user_id)
CREATE POLICY "Users can delete own images"
  ON storage.objects FOR DELETE
  USING (
    bucket_id = 'gallery-images' AND
    auth.uid()::text = (storage.foldername(name))[1]
  );

-- Grant storage permissions to authenticated users
GRANT USAGE ON SCHEMA storage TO authenticated;
GRANT ALL ON SCHEMA storage TO authenticated;
GRANT ALL ON ALL TABLES IN SCHEMA storage TO authenticated;
