-- Refresh PostgREST schema cache
NOTIFY pgrst, 'reload schema';

-- Alternative cache refresh
SELECT pg_notify('pgrst', 'reload schema');

-- Create stored procedure to bypass PostgREST cache
CREATE OR REPLACE FUNCTION save_gallery_item(
  p_user_id TEXT,
  p_dataurl TEXT,
  p_timestamp TEXT,
  p_drawingdata TEXT DEFAULT NULL,
  p_textitemsdata JSONB DEFAULT '[]',
  p_shapeitemsdata JSONB DEFAULT '[]'
)
RETURNS TABLE (
  id BIGINT,
  user_id TEXT,
  dataURL TEXT,
  "timestamp" TEXT,
  drawingData TEXT,
  textItemsData JSONB,
  shapeItemsData JSONB,
  created_at TIMESTAMPTZ,
  updated_at TIMESTAMPTZ
)
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
  RETURN QUERY
  INSERT INTO gallery (
    user_id, 
    dataURL, 
    "timestamp", 
    drawingData, 
    textItemsData, 
    shapeItemsData
  )
  VALUES (
    p_user_id,
    p_dataurl,
    p_timestamp,
    p_drawingdata,
    p_textitemsdata,
    p_shapeitemsdata
  )
  RETURNING *;
END;
$$;

-- Generic SQL execution function to bypass PostgREST
CREATE OR REPLACE FUNCTION execute_sql(sql_query TEXT, params TEXT[] DEFAULT '{}')
RETURNS TABLE (
  id BIGINT,
  user_id TEXT,
  dataURL TEXT,
  "timestamp" TEXT,
  drawingData TEXT,
  textItemsData JSONB,
  shapeItemsData JSONB,
  created_at TIMESTAMPTZ,
  updated_at TIMESTAMPTZ
)
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
  RETURN QUERY
  EXECUTE format(sql_query, params);
