-- ═══════════════════════════════════════════════════════
-- STEP 2: RECREATE GALLERY TABLE
-- ═══════════════════════════════════════════════════════

DROP TABLE IF EXISTS gallery CASCADE;

-- Create gallery table to store user artwork
-- dataURL column stores either:
--   - Supabase Storage public URL (if storage is configured)
--   - Base64 dataURL (fallback if storage is not configured)
CREATE TABLE gallery (
  id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id       TEXT NOT NULL,
  dataURL       TEXT NOT NULL,
  "timestamp"   TEXT NOT NULL,
  drawingData   TEXT,
  textItemsData JSONB DEFAULT '[]',
  shapeItemsData JSONB DEFAULT '[]',
  created_at    TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
  updated_at    TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- ═══════════════════════════════════════════════════════
-- STEP 3: ROW LEVEL SECURITY
-- ═══════════════════════════════════════════════════════

ALTER TABLE gallery ENABLE ROW LEVEL SECURITY;

-- Drop existing policies before recreating
DROP POLICY IF EXISTS "Users can view own gallery"   ON gallery;
DROP POLICY IF EXISTS "Users can insert own gallery" ON gallery;
DROP POLICY IF EXISTS "Users can update own gallery" ON gallery;
DROP POLICY IF EXISTS "Users can delete own gallery" ON gallery;

CREATE POLICY "Users can view own gallery"   ON gallery FOR SELECT USING (true);
CREATE POLICY "Users can insert own gallery" ON gallery FOR INSERT WITH CHECK (true);
CREATE POLICY "Users can update own gallery" ON gallery FOR UPDATE USING (true);
CREATE POLICY "Users can delete own gallery" ON gallery FOR DELETE USING (true);

-- ═══════════════════════════════════════════════════════
-- STEP 4: INDEXES
-- ═══════════════════════════════════════════════════════

CREATE INDEX IF NOT EXISTS gallery_user_id_idx    ON gallery(user_id);
CREATE INDEX IF NOT EXISTS gallery_created_at_idx ON gallery(created_at DESC);

-- ═══════════════════════════════════════════════════════
-- STEP 5: AUTO-UPDATE updated_at TRIGGER
-- ═══════════════════════════════════════════════════════

CREATE OR REPLACE FUNCTION update_updated_at_column()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS update_gallery_updated_at ON gallery;

CREATE TRIGGER update_gallery_updated_at
  BEFORE UPDATE ON gallery
  FOR EACH ROW
  EXECUTE FUNCTION update_updated_at_column();

-- ═══════════════════════════════════════════════════════
-- STEP 6: STORED PROCEDURE (single, canonical version)
-- ═══════════════════════════════════════════════════════

CREATE OR REPLACE FUNCTION save_gallery_item(
  p_user_id       TEXT,
  p_dataurl       TEXT,
  p_timestamp     TEXT,
  p_drawingdata   TEXT    DEFAULT NULL,
  p_textitemsdata  JSONB  DEFAULT '[]',
  p_shapeitemsdata JSONB  DEFAULT '[]'
)
RETURNS TABLE (
  id             BIGINT,
  user_id        TEXT,
  dataURL        TEXT,
  "timestamp"    TEXT,
  drawingData    TEXT,
  textItemsData  JSONB,
  shapeItemsData JSONB,
  created_at     TIMESTAMPTZ,
  updated_at     TIMESTAMPTZ
)
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
  RETURN QUERY
  INSERT INTO gallery (
    user_id,
    dataURL,
    "timestamp",
    drawingData,
    textItemsData,
    shapeItemsData
  )
  VALUES (
    p_user_id,
    p_dataurl,
    p_timestamp,
    p_drawingdata,
    p_textitemsdata,
    p_shapeitemsdata
  )
  RETURNING *;
END;
$$;

